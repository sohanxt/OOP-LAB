using System;

class Program
{
    public static void Main()
    {
        int a = 15, b = 5;
        int add = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;
        int mod = a % b;
        Console.WriteLine("Addition: " + add);
        Console.WriteLine("Subtraction: " + sub);
        Console.WriteLine("Multiplication: " + mul);
        Console.WriteLine("Division: " + div );
        Console.WriteLine("Modulus: " + mod );
    }
}
